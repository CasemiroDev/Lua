--[[
Tipos de dados:
nil -- diferencia o valor de ter alguns dados ou nenhum dado(nulo) 
boolean -- inclui o verdadeiro ou falso como valores
number -- números reais como valores(ponto flutuante de precisão dupla)
string -- matriz de caracteres
function -- representa um método(também usado em C)
userdata -- representa dados C arbitrários
thread -- representa threads de execução independentes e é usado para implementar co-rotinas
table -- representa matrizes comuns, implementa matrizes associativas, contendo qualquer valor (- nil)
]]